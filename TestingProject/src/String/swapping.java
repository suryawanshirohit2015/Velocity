package String;

public class swapping {
	
	public static void main(String[] args) {
		
		int b=45;
		int c=76;
		
		System.out.println("b="+b);
		System.out.println("c="+c);
	  //   b=c;
	    
		
		System.out.println("c="+c);
		System.out.println("*****************");
		int x;
		x=b;   //backup of b in x=45
		b=c;     //76
		c=x;     //45
		
		System.out.println("c="+c);
		System.out.println("b="+b);
		
		
		
		
	}

}
