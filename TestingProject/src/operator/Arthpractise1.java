package operator;

public class Arthpractise1 {
	
	public static void main(String[] args) {
		
		// declare variable
		
		int a = 40, b = 22;
		 
		//addition operator
		
		System.out.println("a + b =" + ( a+b));
		
		//substraction operator   
		
		System.out.println("a - b =" + (a-b));
		
		//multiplication operator 
		
		System.out.println("a * b =" + (a*b));
		
		//division operator
		
		System.out.println("a / b =" + (a/b));
		
		//module operator 
		
		System.out.println("a % b =" + (a%b));
     
		
		
	}
	

}
