package operator;

public class Relational1 {
	
	public static void main(String[] args) {
		
		//variable declaration
		
		int a =20, b=30;
		
		//value of a and b
		
		System.out.println("a is " + a  +  " b is" + b);
		
		// == opearator
		
		System.out.println(a==b);
		
		// !== operator
		
		System.out.println(a!=b);
		
		//>operator
		
		System.out.println(a>b);
		
		//<operator
		
		System.out.println(a<b);
		
		// >= operator
		
		System.out.println(a>=b);
		
		//<= operator
		
		System.out.println(a<=b);
		
		
		
		
		
		
	}

}
