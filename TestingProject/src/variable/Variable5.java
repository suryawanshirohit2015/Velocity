package variable;

public class Variable5 {
	
	public static void main(String[] args) {
		
		//variable declaration
		 
		int u;
		int v;
		char w;
		String x;
		
		//variable initialization
		
		u = 1234;
		v = 5678;
		w = '%';
		x = "windows10.1.64bit@";
		
		//variable usage
		
		System.out.println(u);
		System.out.println(v);
		System.out.println(w);
		System.out.println(x);
		
	}
}
		
		
	
	
	
		
				
		
	


